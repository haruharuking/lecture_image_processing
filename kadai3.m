% 課題３　閾値処理
% 閾値を4パターン設定し,閾値処理た画像を示せ．
% 下記はサンプルプログラムである．
% 課題作成にあたっては「Lenna」以外の画像を用いよ

ORG = imread('outdoor_101_rika.jpg'); %原画像の入力
ORG = rgb2gray(ORG); %カラー画像を白黒濃淡画像へ変換

imagesc(ORG) %カラーマップ（グレー）。カラーバー ％画像の表示一時停止

IMG = ORG >  64 % 輝度値が64以上の画素を1、その他を0に変換
imagesc(IMG) %カラーマップ（グレー）。カラーバー一時停止

IMG=ORG>96
imagesc(IMG) %カラーマップ（グレー）。カラーバー

IMG=ORG>128
imagesc(IMG) %カラーマップ（グレー）。カラーバー一時停止

IMG=ORG>192
